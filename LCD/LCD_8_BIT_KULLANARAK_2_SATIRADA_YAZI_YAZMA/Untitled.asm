	MOVLW A'L'
	CALL LCD_VERI_GONDER
	MOVLW A'L'
	CALL LCD_VERI_GONDER
	MOVLW A'A'
	CALL LCD_VERI_GONDER
	MOVLW A'H'
	CALL LCD_VERI_GONDER
	MOVLW A' '
	CALL LCD_VERI_GONDER
	MOVLW A'T'
	CALL LCD_VERI_GONDER
	MOVLW A'A'
	CALL LCD_VERI_GONDER
	MOVLW A'H'
	CALL LCD_VERI_GONDER
	MOVLW A'T'
	CALL LCD_VERI_GONDER
	MOVLW A'A'
	CALL LCD_VERI_GONDER
	MOVLW A'L'
	CALL LCD_VERI_GONDER
	MOVLW A'I'
	CALL LCD_VERI_GONDER

	CALL SATIR2
	MOVLW A'4'
	CALL LCD_VERI_GONDER
	MOVLW A'1'
	CALL LCD_VERI_GONDER
	MOVLW A' '
	CALL LCD_VERI_GONDER
	MOVLW A' '
