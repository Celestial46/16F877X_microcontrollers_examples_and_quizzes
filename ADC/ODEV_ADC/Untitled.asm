KESME
	BCF PIR1,ADIF
	BANKSEL ADRESH
	MOVFW ADRESH
	BANKSEL PORTB
	MOVWF PORTB
	CALL GECIKME
	BANKSEL ADCON0
	BSF ADCON0,GO
	RETFIE
MAIN
	BANKSEL TRISE
	BSF TRISE,0
	CLRF TRISB
	BANKSEL PORTE
	CLRF PORTE
	CLRF PORTB
	
