	BANKSEL PIR1
	BCF PIR1,ADIF
	BANKSEL PIE1
	BSF PIE1,ADIE
	BANKSEL INTCON
	BSF INTCON,GIE
	BSF INTCON,PEIE
	BANKSEL ADCON0
	BSF ADCON0,GO
DONGU
	GOTO DONGU
	
